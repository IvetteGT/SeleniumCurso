package javabasics;

public class Ejercicio1 {

	public static void main(String[] args) {
		
		System.out.println("Ejercicio 1: SUMA, RESTA, MULTIPLICACION, DIVISIÓN");
		System.out.println("");
		
		int dato1 = 80;
		int dato2 = 20;
		
		System.out.println("Suma de los dos numeros:" + " " + (dato1 + dato2));
		System.out.println("Suma de los dos numeros:" + " " + (dato1 - dato2));
		System.out.println("Suma de los dos numeros:" + " " + (dato1 * dato2));
	}

}
